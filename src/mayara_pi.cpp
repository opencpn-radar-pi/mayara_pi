// A wxTimer that forwards Notify() to a std::function, so the plugin (not a
// wxEvtHandler) can have a periodic heartbeat.
class CallbackTimer : public wxTimer {
 public:
  explicit CallbackTimer(std::function<void()> cb) : m_cb(std::move(cb)) {}
  void Notify() override { if (m_cb) m_cb(); }

 private:
  std::function<void()> m_cb;
};
