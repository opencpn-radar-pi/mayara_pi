// Map an OpenCPN colour scheme to the plugin's UI theme.
static MayaraTheme ThemeFor(PI_ColorScheme cs) {
  MayaraTheme t;  // day / RGB defaults
  switch (cs) {
    case PI_GLOBAL_COLOR_SCHEME_NIGHT:
      t.panel_bg = wxColour(18, 6, 6);
      t.text = wxColour(190, 90, 90);
      t.dim_text = wxColour(120, 60, 60);
      t.lozenge_bg = wxColour(28, 10, 10);
      t.lozenge_border = wxColour(90, 40, 40);
      t.accent = wxColour(215, 80, 80);
      t.accent_dim = wxColour(150, 70, 60);
      t.radar_intensity = 0.5f;
      break;
    case PI_GLOBAL_COLOR_SCHEME_DUSK:
      t.panel_bg = wxColour(20, 20, 26);
      t.text = wxColour(165, 165, 175);
      t.dim_text = wxColour(110, 110, 120);
      t.lozenge_bg = wxColour(22, 22, 28);
      t.lozenge_border = wxColour(70, 70, 80);
      t.accent = wxColour(110, 190, 110);
      t.accent_dim = wxColour(175, 155, 95);
      t.radar_intensity = 0.72f;
      break;
    default:
      break;  // day/rgb use the struct defaults
  }
  return t;
}

// mayara-server is normally found via mDNS (it advertises _signalk-http._tcp).
// MAYARA_SERVER forces a specific address; kMayaraServerFallback is used only
// if discovery finds nothing.
static const char* kMayaraServerFallback = "http://10.56.0.1:6502";

static std::string MayaraExplicitUrl() {
  const char* env = std::getenv("MAYARA_SERVER");
  return (env && *env) ? std::string(env) : std::string();
}

