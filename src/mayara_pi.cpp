// Split the discovered radars into m_windows_count near-even groups. Window w
// gets radars [w*base + min(w,rem) ..], distributing the remainder to the first
// windows. Never more windows than radars.
std::vector<std::vector<int>> mayara_pi::RadarGroups() const {
  std::vector<std::vector<int>> groups;
  const int n = m_client ? m_client->RadarCount() : 0;
  if (n <= 0) return groups;
  const int w = std::max(1, std::min(m_windows_count, n));
  const int base = n / w, rem = n % w;
  int idx = 0;
  for (int g = 0; g < w; ++g) {
    const int count = base + (g < rem ? 1 : 0);
    std::vector<int> grp;
    for (int k = 0; k < count; ++k) grp.push_back(idx++);
    groups.push_back(std::move(grp));
  }
  return groups;
}

void mayara_pi::DestroyWindows(bool sync) {
  bool touched_aui = false;
  for (MayaraPpiWindow* win : m_windows) {
    if (!win) continue;
    win->ShowWindow(false);
    if (win->IsDocked()) {
      if (m_aui) {
        m_aui->DetachPane(win);
        touched_aui = true;
      }
      // sync: delete now so panel timers can't call a freed client during
      // teardown. async: defer (we may be inside a window's own handler).
      if (sync)
        delete win;
      else
        win->Destroy();
    } else if (wxFrame* f = win->HostFrame()) {
      if (sync)
        delete f;  // deletes its child content panel too
      else
        f->Destroy();
    }
  }
  if (touched_aui && m_aui) m_aui->Update();
  m_windows.clear();
}

bool mayara_pi::AnyWindowShown() const {
  for (MayaraPpiWindow* win : m_windows)
    if (win && win->IsWindowShown()) return true;
  return false;
}

void mayara_pi::ApplyThemeToWindows() {
  for (MayaraPpiWindow* win : m_windows)
    if (win) win->ApplyTheme(ThemeFor(m_color_scheme));
}

// (Re)create the radar windows from the current radar count and window count,
// preserving the user's show/hide intent.
void mayara_pi::RebuildWindows() {
  DestroyWindows(/*sync=*/false);
  m_windows_radar_count = m_client ? m_client->RadarCount() : 0;
  std::vector<std::vector<int>> groups = RadarGroups();
  // No radars yet: still open one window bound to index 0 so the user sees the
  // "waiting for radar" UI; it starts showing radar 0 as soon as it connects.
  if (groups.empty()) groups.push_back({0});
  const bool docked = m_docked && m_aui;
  int pane_no = 0;
  for (const std::vector<int>& grp : groups) {
    MayaraPpiWindow* win = nullptr;
    if (docked) {
      win = new MayaraPpiWindow(m_aui->GetManagedWindow(), m_client.get(), grp);
      win->SetDockedHost(m_aui);
      const wxString pane_name = wxString::Format("mayara-%d", pane_no);
      wxAuiPaneInfo pane;
      pane.Name(pane_name)
          .Caption(win->Title())
          .CaptionVisible(true)
          .DestroyOnClose(false)
          .Right()
          .Layer(1)  // outer layer: a full-height column right of the charts
          .Dockable(true)
          .Floatable(true)
          .CloseButton(true)
          .MinSize(300, 240)
          .BestSize(420, 900);
      // Restore this pane's saved AUI layout, then re-assert identity/caption.
      const wxString saved = SavedPaneInfo(pane_no);
      if (!saved.IsEmpty()) {
        m_aui->LoadPaneInfo(saved, pane);
        pane.Name(pane_name).Caption(win->Title()).MinSize(320, 240);
      }
      if (!m_windows_visible) pane.Hide();
      m_aui->AddPane(win, pane);
      ++pane_no;
    } else {
      auto* frame = new wxFrame(m_parent_window, wxID_ANY, wxEmptyString,
                                wxDefaultPosition, wxSize(880, 560));
      win = new MayaraPpiWindow(frame, m_client.get(), grp);
      frame->SetTitle(win->Title());
      frame->SetMinSize(wxSize(480, 320));
      auto* fs = new wxBoxSizer(wxVERTICAL);
      fs->Add(win, 1, wxEXPAND);
      frame->SetSizer(fs);
      win->SetFloatingHost(frame);
      // The plugin owns the window; hide instead of destroying on close.
      frame->Bind(wxEVT_CLOSE_WINDOW, [win](wxCloseEvent& e) {
        win->ShowWindow(false);
        e.Veto();
      });
    }
    win->ApplyTheme(ThemeFor(m_color_scheme));
    win->SetOverlayControl([this]() { return m_overlay_enabled; },
                           [this](bool on) {
                             m_overlay_enabled = on;
                             SaveConfig();
                             GetOCPNCanvasWindow()->Refresh(false);
                           });
    win->SetSettingsControl([this, win]() { ShowSettings(win); });
    win->SetAutoLayoutControl([this]() { AutoLayoutWindows(/*reflow=*/true); });
    win->SetDockControl([this]() { return m_docked; },
                        [this](bool on) {
                          if (on == m_docked) return;
                          m_docked = on;
                          SaveConfig();
                          // Recreate in the new host mode, but not from inside a
                          // window's own control handler.
                          if (m_parent_window)
                            m_parent_window->CallAfter(
                                [this]() { RebuildWindows(); });
                        });
    win->SetNavProvider([this]() { return m_nav; });
    win->SetOrientationHandlers(
        [this](const std::string& id) { return OrientationFor(id); },
        [this](const std::string& id, int o) { SetOrientationFor(id, o); });
    win->ShowWindow(m_windows_visible);
    m_windows.push_back(win);
  }
  if (docked) {
    m_aui->Update();
  } else if (!RestoreWindowGeometry() && m_windows.size() > 1) {
    // Floating: restore saved positions or tile so they don't stack.
    AutoLayoutWindows(/*reflow=*/false);
  }
}

// Tile the radar windows to fill `area`. Landscape: a row-major grid
// (left-right, then next row). Portrait: a single column, top-to-bottom.
static void TileInArea(const std::vector<MayaraPpiWindow*>& wins,
                       const wxRect& area) {
  const int n = static_cast<int>(wins.size());
  if (n == 0) return;
  const bool portrait = area.height > area.width;
  const int cols =
      portrait ? 1 : std::max(1, static_cast<int>(std::ceil(std::sqrt((double)n))));
  const int rows = (n + cols - 1) / cols;
  for (int i = 0; i < n; ++i) {
    const int c = i % cols, r = i / cols;
    const int x0 = area.x + area.width * c / cols;
    const int x1 = area.x + area.width * (c + 1) / cols;
    const int y0 = area.y + area.height * r / rows;
    const int y1 = area.y + area.height * (r + 1) / rows;
    if (wins[i]) wins[i]->SetWindowRect(wxRect(x0, y0, x1 - x0, y1 - y0));
  }
}

void mayara_pi::AutoLayoutWindows(bool reflow_ocpn) {
  if (m_windows.empty() || m_docked) return;  // AUI manages docked panes
  wxWindow* frame =
      m_parent_window ? wxGetTopLevelParent(m_parent_window) : nullptr;
  int main_disp = frame ? wxDisplay::GetFromWindow(frame) : wxNOT_FOUND;
  if (main_disp == wxNOT_FOUND) main_disp = 0;
  const unsigned n_disp = wxDisplay::GetCount();

  if (n_disp > 1) {
    // Put the radars on a display the main window is NOT on and let them share
    // that whole screen; the main window can then fill its own screen.
    unsigned target = (static_cast<unsigned>(main_disp) + 1) % n_disp;
    if (target == static_cast<unsigned>(main_disp)) target = 0;
    TileInArea(m_windows, wxDisplay(target).GetClientArea());
    if (reflow_ocpn && frame) {
      const wxRect ma =
          wxDisplay(static_cast<unsigned>(main_disp)).GetClientArea();
      frame->SetSize(ma.x, ma.y, ma.width, ma.height);
    }
    return;
  }

  // Single display: OpenCPN fills the left, radars stack down the right.
  const wxRect area =
      wxDisplay(static_cast<unsigned>(main_disp)).GetClientArea();
  const int radar_w = std::max(360, area.width * 38 / 100);
  if (reflow_ocpn && frame)
    frame->SetSize(area.x, area.y, area.width - radar_w, area.height);
  TileInArea(m_windows,
             wxRect(area.x + area.width - radar_w, area.y, radar_w,
                    area.height));
}

void mayara_pi::SyncRadarFullScreen(bool on) {
  if (m_docked) return;  // docked panes ride OpenCPN's own full screen
  if (!on) {
    for (MayaraPpiWindow* w : m_windows)
      if (w) w->LeaveFullScreen();
    return;
  }
  wxWindow* frame =
      m_parent_window ? wxGetTopLevelParent(m_parent_window) : nullptr;
  const int ocpn_disp = frame ? wxDisplay::GetFromWindow(frame) : wxNOT_FOUND;
  const unsigned nd = wxDisplay::GetCount();
  for (unsigned d = 0; d < nd; ++d) {
    if (static_cast<int>(d) == ocpn_disp) continue;  // leave OpenCPN's screen
    // Which shown radar windows currently live on display d.
    std::vector<MayaraPpiWindow*> on_d;
    for (MayaraPpiWindow* w : m_windows) {
      if (!w || !w->IsWindowShown()) continue;
      const wxRect r = w->WindowRect();
      const wxPoint mid(r.x + r.width / 2, r.y + r.height / 2);
      if (static_cast<unsigned>(wxDisplay::GetFromPoint(mid)) == d)
        on_d.push_back(w);
    }
    if (on_d.empty()) continue;
    const wxRect area = wxDisplay(d).GetGeometry();  // whole screen
    const int k = static_cast<int>(on_d.size());
    if (k == 1) {
      on_d[0]->EnterFullScreen(area, /*solo=*/true);
      continue;
    }
    // Two or more share the screen: split it (aspect decides the direction).
    const bool portrait = area.height > area.width;
    const int cols =
        portrait ? 1 : std::max(1, static_cast<int>(std::ceil(std::sqrt((double)k))));
    const int rows = (k + cols - 1) / cols;
    for (int i = 0; i < k; ++i) {
      const int c = i % cols, r = i / cols;
      const wxRect t(area.x + area.width * c / cols,
                     area.y + area.height * r / rows, area.width / cols,
                     area.height / rows);
      on_d[i]->EnterFullScreen(t, /*solo=*/false);
    }
  }
}

void mayara_pi::TogglePpiWindow() {
  m_windows_visible = !AnyWindowShown();
  const int radar_count = m_client ? m_client->RadarCount() : 0;
  if (m_windows_visible &&
      (m_windows.empty() || m_windows_radar_count != radar_count))
    RebuildWindows();  // build, or rebuild for a changed radar set
  for (MayaraPpiWindow* win : m_windows)
    if (win) win->ShowWindow(m_windows_visible);
  SetToolbarItemState(m_tool_id, m_windows_visible);
  if (m_windows_visible) CaptureWindowState();
}

bool mayara_pi::RenderGLOverlayMultiCanvas(wxGLContext* pcontext,
                                           PlugIn_ViewPort* vp, int canvasIndex,
                                           int priority) {
  if (!m_overlay_enabled || !m_client || !vp || !vp->bValid) return false;
  // OpenCPN calls this once per priority level per frame. Draw only at the
  // legacy chart-overlay layer; the higher passes (OVER_EMBOSS/OVER_UI) render
  // on top of the toolbar and chart bar, so drawing there paints over them.
  if (priority != OVERLAY_LEGACY) return false;
  const int n = m_client->RadarCount();
  if (n == 0) return false;
  if (static_cast<int>(m_overlay_tex.size()) != n)
    m_overlay_tex.assign(n, OverlayTex{});

  // Collect radars that have data, sorted by range descending so the longest
  // range is drawn first and the shortest composites on top (short-range inner
  // disc, longer-range annulus beyond it).
  struct Item {
    int idx;
    uint32_t range;
  };
  std::vector<Item> items;
  for (int i : m_client->ShownRadars()) {  // at most two displayed radars
    RadarState* st = m_client->StateAt(i);
    if (st && st->RangeMeters() > 0) items.push_back({i, st->RangeMeters()});
  }
  if (items.empty()) return false;
  std::sort(items.begin(), items.end(),
            [](const Item& a, const Item& b) { return a.range > b.range; });

  glEnable(GL_BLEND);
  glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);
  glEnable(GL_TEXTURE_2D);
  glColor4f(m_radar_intensity, m_radar_intensity, m_radar_intensity, 1.0f);

  // Each radar is drawn as an annulus from the next-shorter shown radar's range
  // out to its own range; the shortest is a full disc. So the shorter radar
  // occludes the longer one within its radius.
  bool drew = false;
  for (size_t k = 0; k < items.size(); ++k) {
    const double inner =
        (k + 1 < items.size())
            ? static_cast<double>(items[k + 1].range) / items[k].range
            : 0.0;
    if (DrawRadarOverlay(items[k].idx, vp, inner)) drew = true;
  }

  glBindTexture(GL_TEXTURE_2D, 0);
  glDisable(GL_TEXTURE_2D);
  glDisable(GL_BLEND);
  return drew;
}

bool mayara_pi::DrawRadarOverlay(int index, PlugIn_ViewPort* vp,
                                 double inner_frac) {
  RadarState* state = m_client->StateAt(index);
  if (!state) return false;
  const uint32_t range_m = state->RangeMeters();
  if (range_m == 0) return false;

  double lat = m_ownship_lat, lon = m_ownship_lon;
  if (lat == 0.0 && lon == 0.0)
    if (!state->Position(lat, lon)) return false;

  OverlayTex& t = m_overlay_tex[index];
  if (t.tex == 0) {
    glGenTextures(1, &t.tex);
    t.gen = ~0ull;
  }
  const uint64_t gen = state->Generation();
  if (gen != t.gen) {
    const int size = state->CopyDisc(m_overlay_disc);
    if (size <= 0) return false;
    glBindTexture(GL_TEXTURE_2D, t.tex);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
    glTexImage2D(GL_TEXTURE_2D, 0, GL_RGBA, size, size, 0, GL_RGBA,
                 GL_UNSIGNED_BYTE, m_overlay_disc.data());
    t.gen = gen;
  }

  wxPoint center;
  GetCanvasPixLL(vp, &center, lat, lon);
  if (std::abs(center.x) > 1000000 || std::abs(center.y) > 1000000)
    return false;
  const double radius_px = range_m * vp->view_scale_ppm;
  if (radius_px < 1.0) return false;

  double heading = m_heading_true, sh = 0.0;
  if (heading == 0.0 && state->Heading(sh)) heading = sh;
  const double rot_deg = heading + vp->rotation * 180.0 / M_PI;

  glBindTexture(GL_TEXTURE_2D, t.tex);
  glPushMatrix();
  glTranslatef(center.x, center.y, 0.0f);
  glRotatef(rot_deg, 0.0f, 0.0f, 1.0f);
  glScalef(radius_px, radius_px, 1.0f);

  if (inner_frac <= 0.0) {
    // Full disc. Texcoord = (0.5 + 0.5*vertex).
    glBegin(GL_QUADS);
    glTexCoord2f(0.0f, 0.0f); glVertex2f(-1.0f, -1.0f);
    glTexCoord2f(1.0f, 0.0f); glVertex2f(1.0f, -1.0f);
    glTexCoord2f(1.0f, 1.0f); glVertex2f(1.0f, 1.0f);
    glTexCoord2f(0.0f, 1.0f); glVertex2f(-1.0f, 1.0f);
    glEnd();
  } else {
    // Annulus from inner_frac to 1.0 -- the inner circle is not drawn, so a
    // shorter-range radar occludes this one there.
    const int kSeg = 96;
    const double ri = std::min(0.999, inner_frac);
    glBegin(GL_TRIANGLE_STRIP);
    for (int i = 0; i <= kSeg; ++i) {
      const double a = 2.0 * M_PI * i / kSeg;
      const double c = std::cos(a), s = std::sin(a);
      const float ix = static_cast<float>(ri * c), iy = static_cast<float>(ri * s);
      const float ox = static_cast<float>(c), oy = static_cast<float>(s);
      glTexCoord2f(0.5f + 0.5f * ix, 0.5f + 0.5f * iy); glVertex2f(ix, iy);
      glTexCoord2f(0.5f + 0.5f * ox, 0.5f + 0.5f * oy); glVertex2f(ox, oy);
    }
    glEnd();
  }
  glPopMatrix();
  return true;
}

void mayara_pi::SetPositionFixEx(PlugIn_Position_Fix_Ex& pfix) {
  m_ownship_lat = pfix.Lat;
  m_ownship_lon = pfix.Lon;
  m_ownship_cog = pfix.Cog;
  m_heading_true = pfix.Hdt;

  // NaN-tolerant fill of the shared nav state for the PPI overlays.
  m_nav.lat = pfix.Lat;
  m_nav.lon = pfix.Lon;
  m_nav.valid = !std::isnan(pfix.Lat) && !std::isnan(pfix.Lon) &&
                (pfix.Lat != 0.0 || pfix.Lon != 0.0);
  m_nav.has_cog = !std::isnan(pfix.Cog) && pfix.Cog >= 0.0;
  m_nav.cog = m_nav.has_cog ? pfix.Cog : 0.0;
  m_nav.sog = std::isnan(pfix.Sog) ? 0.0 : pfix.Sog;
  m_nav.has_hdt = !std::isnan(pfix.Hdt) && pfix.Hdt >= 0.0 && pfix.Hdt < 360.0;
  m_nav.hdt = m_nav.has_hdt ? pfix.Hdt : 0.0;
}

void mayara_pi::SetColorScheme(PI_ColorScheme cs) {
  m_color_scheme = cs;
  const MayaraTheme t = ThemeFor(cs);
  m_radar_intensity = t.radar_intensity;
  if (m_client) m_client->SetAllIntensity(t.radar_intensity);
  ApplyThemeToWindows();
}
