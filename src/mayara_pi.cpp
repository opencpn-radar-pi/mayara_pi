int mayara_pi::Init() {
  m_parent_window = GetOCPNCanvasWindow();
  m_aui = GetFrameAuiManager();  // may be null in some builds
  LoadConfig();

  m_tool_id = InsertPlugInTool(
      _("Mayara"), &m_tool_bitmap, &m_tool_bitmap, wxITEM_CHECK,
      _("Mayara Radar"), _("Show or hide the Mayara radar PPI window"),
      nullptr, -1, 0, this);

  // Canvas right-click context-menu items (checkable), mirroring radar_pi.
  {
    m_mi_overlay_item = new wxMenuItem(
        nullptr, wxID_ANY, _("Mayara radar overlay"), wxEmptyString,
        wxITEM_CHECK);
    m_mi_overlay = AddCanvasContextMenuItem(m_mi_overlay_item, this);
    m_mi_ppi_item = new wxMenuItem(nullptr, wxID_ANY, _("Mayara PPI window"),
                                   wxEmptyString, wxITEM_CHECK);
    m_mi_ppi = AddCanvasContextMenuItem(m_mi_ppi_item, this);
  }

  m_client = std::make_unique<MayaraClient>(MayaraExplicitUrl(),
                                            kMayaraServerFallback);
  m_client->Start();

  // 1 Hz heartbeat: re-open windows persisted as shown once radars appear, and
  // keep a live geometry snapshot for saving (independent of a GPS fix).
  m_heartbeat = std::make_unique<CallbackTimer>([this]() {
    if (m_windows_visible && m_windows.empty() && m_client &&
        m_client->RadarCount() > 0) {
      RebuildWindows();
      if (m_tool_id != -1) SetToolbarItemState(m_tool_id, true);
    }
    // Follow OpenCPN in/out of full screen.
    const bool fs = GetFullScreen();
    if (fs != m_ocpn_fullscreen) {
      m_ocpn_fullscreen = fs;
      SyncRadarFullScreen(fs);
    }
    // Snapshot geometry for persistence, but not while full-screen (that would
    // save the full-screen rects).
    if (AnyWindowShown() && !m_ocpn_fullscreen) CaptureWindowState();
  });
  m_heartbeat->Start(1000);

  // Capabilities. The overlay callback is declared now but returns false until
  // Phase 1 wires up the spoke renderer.
  return WANTS_OPENGL_OVERLAY_CALLBACK | WANTS_OVERLAY_CALLBACK |
         WANTS_TOOLBAR_CALLBACK | INSTALLS_TOOLBAR_TOOL | WANTS_CURSOR_LATLON |
         WANTS_PREFERENCES | USES_AUI_MANAGER;
}

bool mayara_pi::DeInit() {
  // Destroy the window first (synchronously) so its panels' timers stop calling
  // into the client before we free it.
  if (m_heartbeat) {
    m_heartbeat->Stop();
    m_heartbeat.reset();
  }
  SaveWindowState();  // remember visibility + positions before tearing down
  DestroyWindows(/*sync=*/true);
  if (m_tool_id != -1) {
    RemovePlugInTool(m_tool_id);
    m_tool_id = -1;
  }
  if (m_mi_overlay != -1) RemoveCanvasContextMenuItem(m_mi_overlay);
  if (m_mi_ppi != -1) RemoveCanvasContextMenuItem(m_mi_ppi);
  m_mi_overlay = m_mi_ppi = -1;
  if (m_client) {
    m_client->Stop();
    m_client.reset();
  }
  // Drain any pending (possibly cross-thread) log records now, while this
  // plugin's dylib -- and the string literals its log records point at -- is
  // still mapped. Otherwise OpenCPN flushes them on a later idle tick after
  // unloading us, dereferencing freed memory in OcpnLog::DoLogRecord.
  wxLog::FlushActive();
  return true;
}

