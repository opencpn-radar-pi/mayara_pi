static const char* kConfigGroup = "/PlugIns/mayara";

void mayara_pi::LoadConfig() {
  wxFileConfig* cfg = GetOCPNConfigObject();
  if (!cfg) return;
  cfg->SetPath(kConfigGroup);
  int windows = 1;
  cfg->Read("WindowsCount", &windows, 1);
  m_windows_count = windows < 1 ? 1 : windows;
  bool overlay = true;
  cfg->Read("OverlayEnabled", &overlay, true);
  m_overlay_enabled = overlay;
  // Per-radar orientation: "id=mode;id=mode;..."
  m_orient.clear();
  wxString orient;
  if (cfg->Read("Orientations", &orient) && !orient.IsEmpty()) {
    wxStringTokenizer tok(orient, ";");
    while (tok.HasMoreTokens()) {
      const wxString pair = tok.GetNextToken();
      const int eq = pair.Find('=');
      if (eq == wxNOT_FOUND) continue;
      long m = 0;
      if (pair.Mid(eq + 1).ToLong(&m) && m >= 0 && m <= 2)
        m_orient[std::string(pair.Left(eq).mb_str())] = static_cast<int>(m);
    }
  }
  bool shown = false;
  cfg->Read("WindowsVisible", &shown, false);
  m_windows_visible = shown;  // restored on the next fix (see SetPositionFixEx)
  bool docked = false;
  cfg->Read("Docked", &docked, false);
  m_docked = docked;
}

int mayara_pi::OrientationFor(const std::string& radar_id) const {
  auto it = m_orient.find(radar_id);
  return it != m_orient.end() ? it->second : 0;
}

void mayara_pi::SetOrientationFor(const std::string& radar_id, int mode) {
  m_orient[radar_id] = mode;
  SaveConfig();
}

// Snapshot geometry + visibility while the windows are definitely alive. wx may
// destroy top-level windows before DeInit, so we never read them at shutdown.
void mayara_pi::CaptureWindowState() {
  if (m_docked) {
    // Docked: remember each pane's AUI layout (dock side, float pos/size, ...).
    m_persp_cache.clear();
    if (m_aui)
      for (MayaraPpiWindow* w : m_windows) {
        if (!w) continue;
        wxAuiPaneInfo& pane = m_aui->GetPane(w);
        m_persp_cache.push_back(pane.IsOk() ? m_aui->SavePaneInfo(pane)
                                            : wxString());
      }
    return;
  }
  m_geom_cache.clear();
  for (MayaraPpiWindow* w : m_windows)
    if (w) m_geom_cache.push_back(w->WindowRect());
}

void mayara_pi::SaveWindowState() {
  wxFileConfig* cfg = GetOCPNConfigObject();
  if (!cfg) return;
  cfg->SetPath(kConfigGroup);
  cfg->Write("WindowsVisible", m_windows_visible);
  cfg->Write("WinCount", static_cast<int>(m_geom_cache.size()));
  for (size_t i = 0; i < m_geom_cache.size(); ++i) {
    const wxRect& r = m_geom_cache[i];
    const int k = static_cast<int>(i);
    cfg->Write(wxString::Format("Win%d_x", k), r.x);
    cfg->Write(wxString::Format("Win%d_y", k), r.y);
    cfg->Write(wxString::Format("Win%d_w", k), r.width);
    cfg->Write(wxString::Format("Win%d_h", k), r.height);
  }
  // Docked pane layouts (AUI perspective per pane). The "R" key version was
  // bumped when the default dock moved to a full-height right column.
  cfg->Write("PaneRCount", static_cast<int>(m_persp_cache.size()));
  for (size_t i = 0; i < m_persp_cache.size(); ++i)
    cfg->Write(wxString::Format("PaneR%d", static_cast<int>(i)),
               m_persp_cache[i]);
  cfg->Flush();
}

bool mayara_pi::RestoreWindowGeometry() {
  wxFileConfig* cfg = GetOCPNConfigObject();
  if (!cfg) return false;
  cfg->SetPath(kConfigGroup);
  int wc = 0;
  cfg->Read("WinCount", &wc, 0);
  if (wc <= 0 || wc != static_cast<int>(m_windows.size())) return false;
  for (size_t i = 0; i < m_windows.size(); ++i) {
    if (!m_windows[i]) continue;
    const int k = static_cast<int>(i);
    int x, y, w, h;
    if (cfg->Read(wxString::Format("Win%d_x", k), &x) &&
        cfg->Read(wxString::Format("Win%d_y", k), &y) &&
        cfg->Read(wxString::Format("Win%d_w", k), &w) &&
        cfg->Read(wxString::Format("Win%d_h", k), &h) && w > 80 && h > 80)
      m_windows[i]->SetWindowRect(wxRect(x, y, w, h));
  }
  return true;
}

wxString mayara_pi::SavedPaneInfo(int index) const {
  wxFileConfig* cfg = GetOCPNConfigObject();
  if (!cfg) return wxString();
  cfg->SetPath(kConfigGroup);
  int pc = 0;
  cfg->Read("PaneRCount", &pc, 0);
  if (index < 0 || index >= pc) return wxString();
  wxString s;
  cfg->Read(wxString::Format("PaneR%d", index), &s);
  return s;
}

void mayara_pi::SaveConfig() {
  wxFileConfig* cfg = GetOCPNConfigObject();
  if (!cfg) return;
  cfg->SetPath(kConfigGroup);
  cfg->Write("WindowsCount", m_windows_count);
  cfg->Write("OverlayEnabled", m_overlay_enabled);
  wxString orient;
  for (const auto& kv : m_orient)
    orient += wxString::Format("%s=%d;", kv.first.c_str(), kv.second);
  cfg->Write("Orientations", orient);
  cfg->Write("Docked", m_docked);
  cfg->Flush();
}

// OpenCPN's "Preferences" button in the plugin manager routes here; our own
// gear button routes here too. Both hand us a parent to centre on.
void mayara_pi::ShowPreferencesDialog(wxWindow* parent) { ShowSettings(parent); }

void mayara_pi::ShowSettings(wxWindow* parent) {
  const int radar_count = m_client ? m_client->RadarCount() : 0;
  // With N radars you can spread them across 1..N windows; the per-window count
  // is N / windows (rounded up). Cap the spinner sensibly when nothing is found.
  const int max_windows = radar_count > 0 ? radar_count : 4;

  wxDialog dlg(parent, wxID_ANY, _("Mayara Radar Settings"), wxDefaultPosition,
               wxDefaultSize, wxDEFAULT_DIALOG_STYLE);
  auto* top = new wxBoxSizer(wxVERTICAL);

  wxString summary =
      radar_count > 0
          ? wxString::Format(_("%d radar(s) discovered."), radar_count)
          : _("No radars discovered yet.");
  top->Add(new wxStaticText(&dlg, wxID_ANY, summary), 0, wxALL, 10);

  auto* row = new wxBoxSizer(wxHORIZONTAL);
  row->Add(new wxStaticText(&dlg, wxID_ANY, _("Number of PPI windows:")), 0,
           wxALIGN_CENTER_VERTICAL | wxRIGHT, 8);
  auto* spin = new wxSpinCtrl(&dlg, wxID_ANY, wxEmptyString, wxDefaultPosition,
                              wxDefaultSize, wxSP_ARROW_KEYS, 1, max_windows,
                              m_windows_count);
  row->Add(spin, 0, wxALIGN_CENTER_VERTICAL);
  top->Add(row, 0, wxLEFT | wxRIGHT | wxBOTTOM, 10);

  auto* hint = new wxStaticText(
      &dlg, wxID_ANY,
      _("Radars are spread evenly across the windows. One window shows all "
        "radars; more windows split them (e.g. 8 radars across 2 windows = 4 "
        "per window)."));
  hint->Wrap(360);
  top->Add(hint, 0, wxLEFT | wxRIGHT | wxBOTTOM, 10);

  top->Add(dlg.CreateButtonSizer(wxOK | wxCANCEL), 0,
           wxALIGN_RIGHT | wxALL, 10);
  dlg.SetSizerAndFit(top);

  if (dlg.ShowModal() == wxID_OK) {
    const int chosen = spin->GetValue();
    if (chosen != m_windows_count) {
      m_windows_count = chosen;
      SaveConfig();
      // Re-lay-out the radars across the new number of windows, keeping the
      // current show/hide intent.
      if (!m_windows.empty() || m_windows_visible) RebuildWindows();
    }
  }
}

